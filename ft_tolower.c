#include "libft.h"

char	*ft_tolower(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (c >= 'A' && c <= 'Z')
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}