#include "libft.h"

int	ft_isdigit(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(('0' <= str[i]) && (str[i] <= '9')))
		{
			return (0);
		}
		i++;
	}
	return (1);
}